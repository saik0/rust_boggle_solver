pub const ALPHABET_SIZE: usize =  26;

pub fn ascii_byte_to_idx(b: u8) -> usize {
    (b - 'a' as u8) as usize
}


pub fn is_alpha(s: &str) -> bool {
    s.chars().all(|c| match c {
        'a' ... 'z' => true,
        'A' ... 'Z' => true,
        _ => false
    })
}
